// +build pulse

package pulse

////////////////////////////////////////////////////////////////////////////////
// CGO

/*
#cgo pkg-config: libpulse
#include <pulse/error.h>
#include <stdlib.h>
*/
import "C"

////////////////////////////////////////////////////////////////////////////////
// TYPES

type (
	PulseError int
)

////////////////////////////////////////////////////////////////////////////////
// ERROR IMPLEMENTATION

func (err PulseError) Error() string {
	if err == 0 {
		return "Success"
	} else {
		return C.GoString(C.pa_strerror(C.int(err)))
	}
}

